int	ft_is_prime(int n)
{
	int	div;
	int	count;

	div = 1;
	count = 0;
	while (div <= n)
	{
		if (n % div == 0)
			count++;
		div++;
	}
	if (count == 2)
		return (1);
	return (0);
}
